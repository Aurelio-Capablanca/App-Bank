package com.bankapp.bank.Models;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.Setter;
import lombok.ToString;

import javax.persistence.*;

@Entity
@Table(name = "banks")
@ToString
@EqualsAndHashCode
public class banks {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)

    @Getter
    @Setter
    @Column(name = "id_bank")
    private int id_bank;

    @Getter
    @Setter
    @Column(name = "name_bank")
    private String name_bank;

    @Getter
    @Setter
    @Column(name = "direction_bank")
    private String direction_bank;

    @Getter
    @Setter
    @Column(name = "phone_bank")
    private String phone_bank;

    @Getter
    @Setter
    @Column(name = "president_bank")
    private String president_bank;

    @Getter
    @Setter
    @Column(name = "id_account")
    private int id_account;

}
